from enum import Enum, auto

class ShipStatus(Enum):
    """
    Статусы жизненного цикла корабля (состояния конечного автомата).
    Определяют, какие операции доступны в данный момент.
    """
    IN_DESIGN = auto()             # Стадия проектирования (чертежи)
    UNDER_CONSTRUCTION = auto()    # Строительство (установка модулей, корпуса, двигателя)
    EQUIPPING = auto()             # Экипировка (набор команды, загрузка припасов)
    READY_FOR_VOYAGE = auto()      # Готов к отплытию (все проверки безопасности пройдены)
    IN_NAVIGATION = auto()         # В открытом море (плавание)
    MAINTENANCE = auto()           # В доке на техническом обслуживании

class CrewRole(Enum):
    """
    Роли членов экипажа на судне. 
    Используются для проверки минимально необходимого состава перед отплытием.
    """
    CAPTAIN = auto()               # Капитан (обязателен для навигации)
    NAVIGATOR = auto()             # Штурман (прокладка курса на командном мостике)
    ENGINEER = auto()              # Главный механик (обязателен для контроля двигателя)
    SAILOR = auto()                # Матрос (палубная команда)
    COOK = auto()                  # Судовой повар

class EngineType(Enum):
    """
    Типы судовых двигателей (для модуля Engine).
    """
    DIESEL = auto()                # Дизельный (стандартный для грузовых судов)
    STEAM_TURBINE = auto()         # Паротурбинный
    NUCLEAR = auto()               # Ядерный (для ледоколов или крупных авианосцев)
    ELECTRIC = auto()              # Электродвигатель