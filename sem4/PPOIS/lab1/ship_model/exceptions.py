class ShipError(Exception):
    """
    Базовый класс для всех исключений корабельной системы.
    Все остальные исключения наследуются от него.
    Это позволяет отлавливать любую ошибку домена одним блоком except ShipError.
    """
    def __init__(self, message: str) -> None:
        self.message = message
        super().__init__(self.message)


class ValidationError(ShipError):
    """
    Ошибки валидации входных данных.
    Используется, когда переданы неверные типы данных, пустые строки 
    или отрицательные значения (например, отрицательная мощность двигателя).
    """
    def __init__(self, field: str, detail: str) -> None:
        message = f"Ошибка валидации поля '{field}': {detail}"
        super().__init__(message)


class ConstructionError(ShipError):
    """
    Ошибки конечного автомата на этапах IN_DESIGN и UNDER_CONSTRUCTION.
    Например: попытка построить корабль, который уже построен.
    """
    pass


class CrewError(ShipError):
    """
    Ошибки, связанные с наймом и составом экипажа.
    Например: попытка нанять экипаж в открытом море или нехватка капитана.
    """
    pass


class NavigationError(ShipError):
    """
    Ошибки навигации и управления конечным автоматом на этапе плавания.
    Например: попытка выйти в море с неисправным двигателем или пришвартоваться, 
    находясь в доке.
    """
    pass


class SubsystemError(ShipError):
    """
    Ошибки внутреннего состояния конкретных подсистем судна (двигатель, мост, борт).
    Например: попытка запустить уже работающий двигатель или проложить курс 
    без калибровки систем.
    """
    pass


class CapacityError(ShipError):
    """
    Ошибки превышения различных лимитов.
    Например: попытка погрузить груз или посадить людей сверх лимита корпуса.
    """
    def __init__(self, limit: float, attempted: float) -> None:
        message = f"Превышен лимит вместимости. Максимум: {limit}, попытка загрузки: {attempted}"
        super().__init__(message)